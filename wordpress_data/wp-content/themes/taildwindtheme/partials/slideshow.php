<script>
    $(document).ready(function() {
        animateShapes();
    })

    function animateShapes() {
        const shapes = document.querySelectorAll('.slide-right-shapes span');

        shapes.forEach(shape => {
            shape.style.transition = 'none';
            shape.style.transform = 'rotateY(-60deg)';
        });

        void shapes[0].offsetWidth;

        shapes.forEach(shape => {
            shape.style.transition = 'transform 1.4s ease';
            shape.style.transform = 'rotateY(0deg)';
        });
    }
    const shapes = document.querySelectorAll('.slide-right-shapes span');
    shapes.forEach((shape, index) => {
        shape.style.transitionDelay = `${index * 0.2}s`;
    });
</script>
<script>
    const sliders = document.querySelectorAll('.hero-slider');

    sliders.forEach((sliderEl) => {
        new Swiper(sliderEl, {
            loop: true,
            speed: 1200,
            spaceBetween: 30,
            effect: 'fade',
            navigation: {
                nextEl: sliderEl.querySelector('.hero-next'),
                prevEl: sliderEl.querySelector('.hero-prev'),
            },
            autoplay: {
                delay: 5000,
                disableOnInteraction: false,
            },
        });

        // Add click event listeners to navigation buttons to call animateShapes
        const nextButtons = document.querySelectorAll('.hero-next');
        const prevButtons = document.querySelectorAll('.hero-prev');

        nextButtons.forEach(btn => {
            btn.addEventListener('click', () => {
                animateShapes();
            });
        });

        prevButtons.forEach(btn => {
            btn.addEventListener('click', () => {
                animateShapes();
            });
        });
    });
</script>